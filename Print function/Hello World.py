# print() function is used to print output on screen. We can use double or single quotes.
print("Hello World!")
# What is the correct extension of a python file.
# Answer - .py
# Print "Python is fun!"
print("Python is fun!" )
# Print multiple statement
print("Hello World!")
print("Have a good day.")
print("Welcome to python learning")
# We can also print multiple statement by the use of semi-colon (rarely used)
print("Hello World!"); print("Have a great day.")
#Print the value without a new line.
print("Hello World!", end="") # Default value is /n i.e new line
print(("Welcome to python learning."))
# Print statement can also used to display a number. Only make sure that, it should not written in single or double quotes.
print(30) # positive integer
print(-256) # negative integer
print(0) # zero
print(89.0976) #float
print(True) # boolean
print(None) # Empty value
#We can also perform mathematical operation using numbers in print() function. It will return the output.
print(10+30)
# We can also write down the text and number under the print() function.
print("I am",29,"year old!.")

